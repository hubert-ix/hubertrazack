      <div class="lyrics">
        Over the hills of April
        <br />With soft winds hand in hand,
        <br />Impassionate and dreamy-eyed,
        <br />Spring leads her saraband.
        <br />Her garments float and gather
        <br />And swirl along the plain,
        <br />Her headgear is the golden sun,
        <br />Her cloak the silver rain.
        <br /><br />With colour and with music,
        <br />With perfumes and with pomp,
        <br />By meadowland and upland,
        <br />Through pasture, wood, and swamp,
        <br />With promise and enchantment
        <br />Leading her mystic mime,
        <br />She comes to lure the world anew
        <br />With joy as old as time.
        <br /><br />Quick lifts the marshy chorus
        <br />To transport, trill on trill;
        <br />There's not a rod of stony ground
        <br />Unanswering on the hill.
        <br />The brooks and little rivers
        <br />Dance down their wild ravines,
        <br />And children in the city squares
        <br />Keep time, to tambourines.
        <br /><br />The blue bird in the orchard
        <br />Is lyrical for her,
        <br />The starling with his meadow pipe
        <br />Sets all the wood astir,
        <br />The hooded white spring-beauties
        <br />Are curtsying in the breeze,
        <br />The blue hepaticas are out
        <br />Under the chestnut trees.
        <br /><br />O hark, hear thou the summons,
        <br />Put every grief away,
        <br />When all the motley masques of earth
        <br />Are glad upon a day.
        <br />Alack, that any mortal
        <br />Should less than gladness bring
        <br />Into the choral joy that sounds
        <br />The saraband of spring!
      </div>
      <div class="piece-author">
        Poem by Bliss Carman
      </div>
